package com.learn;
//Swap varibale
public class Assign1 {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        System.out.println("a="+a);
        System.out.println("b="+b);
        int t=a;
        a=t;
        b=t;
        System.out.println("a="+a);
        System.out.println("b="+b);
        System.out.println("t = " + t);


    }
}
